const { PrismaClient } = require('@prisma/client');

async function verifyRegistrationData() {
  const prisma = new PrismaClient();
  
  try {
    console.log('üîç VERIFICANDO DATOS DE REGISTRO EN BASE DE DATOS...\n');
    
    // Primero verificar qu√© campos existen
    console.log('üìã Verificando estructura de la tabla Client...');
    
    // Obtener todos los clientes con campos b√°sicos que sabemos que existen
    const clients = await prisma.client.findMany({
      orderBy: {
        createdAt: 'desc'
      }
    });
    
    console.log(`üìä TOTAL DE CLIENTES REGISTRADOS: ${clients.length}\n`);
    
    if (clients.length === 0) {
      console.log('‚ùå No hay clientes registrados en la base de datos');
      return;
    }
    
    // Mostrar datos de cada cliente (todos los campos disponibles)
    clients.forEach((client, index) => {
      console.log(`üë§ CLIENTE ${index + 1}:`);
      console.log('   DATOS COMPLETOS:');
      
      // Mostrar todos los campos del cliente
      Object.entries(client).forEach(([key, value]) => {
        if (key === 'password') {
          console.log(`   üîí ${key}: [OCULTO POR SEGURIDAD]`);
        } else if (key === 'apiKey') {
          console.log(`   üîë ${key}: ${value ? '[EXISTE]' : '[NO DEFINIDO]'}`);
        } else if (value instanceof Date) {
          console.log(`   üìÖ ${key}: ${value.toLocaleString()}`);
        } else if (value === null || value === undefined) {
          console.log(`   ‚ö™ ${key}: [VACI√ì]`);
        } else {
          console.log(`   ‚úÖ ${key}: "${value}"`);
        }
      });
      
      console.log('   ' + '‚îÄ'.repeat(60));
    });
    
    // Verificar el cliente m√°s reciente
    if (clients.length > 0) {
      const latestClient = clients[0];
      console.log('\nüÜï √öLTIMO CLIENTE REGISTRADO:');
      
      // Mostrar campos principales si existen
      if (latestClient.email) console.log(`   üìß Email: ${latestClient.email}`);
      if (latestClient.companyName) console.log(`   üè¢ Empresa: ${latestClient.companyName}`);
      if (latestClient.createdAt) console.log(`   üìÖ Registrado: ${latestClient.createdAt.toLocaleString()}`);
      
      console.log('\nüîç AN√ÅLISIS DE CAMPOS:');
      
      // Contar campos no vac√≠os
      const allFields = Object.keys(latestClient);
      const filledFields = allFields.filter(field => {
        const value = latestClient[field];
        return value !== null && value !== undefined && value !== '';
      });
      
      console.log(`   üìä Campos totales: ${allFields.length}`);
      console.log(`   ‚úÖ Campos con datos: ${filledFields.length}`);
      console.log(`   üìà Completitud: ${((filledFields.length / allFields.length) * 100).toFixed(1)}%`);
      
      // Mostrar campos vac√≠os
      const emptyFields = allFields.filter(field => {
        const value = latestClient[field];
        return value === null || value === undefined || value === '';
      });
      
      if (emptyFields.length > 0) {
        console.log(`\n‚ö™ CAMPOS VAC√çOS (${emptyFields.length}):`);
        emptyFields.forEach(field => console.log(`   - ${field}`));
      }
      
      // Mostrar campos con datos
      console.log(`\n‚úÖ CAMPOS CON DATOS (${filledFields.length}):`);
      filledFields.forEach(field => {
        const value = latestClient[field];
        if (field === 'password') {
          console.log(`   - ${field}: [PROTEGIDO]`);
        } else if (field === 'apiKey') {
          console.log(`   - ${field}: [EXISTE]`);
        } else {
          console.log(`   - ${field}: "${value}"`);
        }
      });
    }
    
  } catch (error) {
    console.error('‚ùå Error verificando datos:', error.message);
    console.error('Stack:', error.stack);
  } finally {
    await prisma.$disconnect();
  }
}

/**
 * Verificar consistencia de campos entre formularios y base de datos
 */
async function verifyFieldConsistency() {
  console.log('\nüîç ===== VERIFICANDO CONSISTENCIA DE CAMPOS =====');
  
  try {
    // Obtener esquema de la tabla Client
    const tableInfo = await prisma.$queryRaw`
      SELECT column_name, data_type, is_nullable 
      FROM information_schema.columns 
      WHERE table_name = 'Client' 
      ORDER BY column_name;
    `;
    
    console.log('\nüìã CAMPOS EN BASE DE DATOS:');
    const dbFields = {};
    tableInfo.forEach(col => {
      dbFields[col.column_name] = {
        type: col.data_type,
        nullable: col.is_nullable === 'YES'
      };
      console.log(`  ‚úÖ ${col.column_name}: ${col.data_type} ${col.is_nullable === 'YES' ? '(nullable)' : '(required)'}`);
    });
    
    // Verificar campos cr√≠ticos para registro
    const criticalFields = [
      'email', 'companyName', 'companyDescription', 
      'industry', 'phone', 'address', 'website'
    ];
    
    console.log('\nüîç VERIFICANDO CAMPOS CR√çTICOS:');
    let missingFields = [];
    
    criticalFields.forEach(field => {
      if (dbFields[field]) {
        console.log(`  ‚úÖ ${field}: Existe en BD`);
      } else {
        console.log(`  ‚ùå ${field}: FALTA en BD`);
        missingFields.push(field);
      }
    });
    
    if (missingFields.length > 0) {
      console.log(`\n‚ö†Ô∏è CAMPOS FALTANTES: ${missingFields.join(', ')}`);
      console.log('üí° Ejecuta la migraci√≥n: ALTER TABLE "Client" ADD COLUMN "companyDescription" TEXT;');
    } else {
      console.log('\n‚úÖ Todos los campos cr√≠ticos est√°n presentes');
    }
    
    // Mapeo de campos entre formularios
    console.log('\nüìù MAPEO DE CAMPOS:');
    const fieldMapping = {
      'email': { registration: 'email', botConfig: 'contact_email', db: 'email' },
      'phone': { registration: 'contactPhone', botConfig: 'main_phone', db: 'phone' },
      'companyName': { registration: 'companyName', botConfig: 'company_name', db: 'companyName' },
      'companyDescription': { registration: 'companyDescription', botConfig: 'company_description', db: 'companyDescription' },
      'industry': { registration: 'businessSector', botConfig: 'industry', db: 'industry' }
    };
    
    Object.entries(fieldMapping).forEach(([field, mapping]) => {
      console.log(`  üîÑ ${field}:`);
      console.log(`    üìù Registro: ${mapping.registration}`);
      console.log(`    ‚öôÔ∏è Bot Config: ${mapping.botConfig}`);
      console.log(`    üóÑÔ∏è Base de Datos: ${mapping.db}`);
    });
    
    return { dbFields, missingFields, fieldMapping };
    
  } catch (error) {
    console.error('‚ùå Error verificando consistencia:', error);
    throw error;
  }
}

